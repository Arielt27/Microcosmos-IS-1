    private void actualizarTabla()
    {
        t = (DefaultTableModel)jTable_DetalleCompra.getModel();
        t.setRowCount(0);
        jTable_DetalleCompra.setModel(t);
        
        String IdCompra = FmrHistorialFacturas.idCompra;
        
        int CompraId = Integer.parseInt(IdCompra);
        
        int idCompra = CompraId;
                               
        List<DetalleCompra> dCompra = this.daoDetalle.findDetalleCompraEntities();
        
        String s = null;
        for(DetalleCompra DetalleCompra : dCompra)
        {
            if(DetalleCompra.getIdCompra() == idCompra)
            {                
                t.addRow(
                    new Object[]{
                        DetalleCompra.getIdCompra(),                        
                        GetNombreArticulo(DetalleCompra.getIdArticulo()),
                        DetalleCompra.getCantidad(),
                        s                       
                    });                
            }
        }        
    }
    
    private static String GetNombreArticulo(int id)
    {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("DB");
        EntityManager em = emf.createEntityManager();
        String select = "SELECT NombreArticulo FROM Articulo WHERE IdArticulo = '"+ id+ "'";
        Query query = em.createQuery(select);
    
        return query.getSingleResult().toString() ;            
    }         
    
    
