    private void actualizar()
    {    
        t = (DefaultTableModel)jTable_DetallesVentas.getModel();
        t.setRowCount(0);           
        jTable_DetallesVentas.setModel(t); 
        
        List<Venta> venta = this.daoVenta.findVentaEntities();
        
        String s = null;
        for(Venta Venta : venta)
        {                                                 
            t.addRow(
                    new Object[]{
                        Venta.getIdVenta(),
                        Venta.getFechaVenta(),
                        Venta.getTotal(), 
                        Venta.getIdEmpleados(),                        
                        s                       
                    });            
        }
    }
    
    
